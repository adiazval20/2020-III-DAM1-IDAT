class Calculadora {
    public int sumar(int x, int y) {
        return x + y;
    }

    public int sumar(int x, int y, int z) {
        return x + y + z;
    }

    public int restar(int x, int y) {
        return x - y;
    }

    public int multiplicar(int x, int y) {
        return x * y;
    }

    public int dividir(int x, int y) {
        return x / y;
    }
}